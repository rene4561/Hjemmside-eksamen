                    <h2>Value Propersition Canvas</h2>
                    <p>Efter at have forstået Business Model Canvas, som hjælper en med at skabe værdi for virksomheden. Dertil kan man se på Value Proposition Canvas, denne hjælper virksomheden med at skabe værdi for kunderne. Dette er meget væsentlig, da kundesegmentet er hele grundlaget for virksomheden.</p>
                    <img src="Modeller/Value%20map.png" alt="Illustration af Valua proposition i business model canvas'et">
                    <p>Denne model er delt op i 2 segmenter: value map og customer profile - og hvis man skal starte helt fra starten, og skabe et nyt produkt, vil man først kigge efter customer jobs. Dette er ikke nødvendigvis deres konkrete arbejde. Det kan nærmere beskrives, som en opgave de prøver at få løst.</p>
                    <p>For at få denne opgave løst vil der være en række pains. Disse er de risici, forhindringer mm. som kunden oplever, mens de prøver at løse opgaven.</p>
                    <p>I value map tilbyder virksomheden kunderne et produkt eller en ydelse, som kan fungere som pain relivers - eller smertestilne. Her skal man prøve at forklare hvad produktet kan gøre, for at dæmpe den smerte, som kunden oplever – altså løse deres pains. </p>
                    <p>Oppe i gain creators beskriver man de positive effekter ens pain relievers giver kunden. </p>
                    <p>Dernæst trækker man disse effekter over til kunden, hvor man beskriver de konkrete fordele de får, ved at anvende produktet. </p>
                    <img src="Modeller/Value%20canvas.png" alt="Value Proposition Canvas - her ses relationen mellem produkt og forbruger">
                    <p>Hvis begge segmenter stemmer overens, har man det man kalder et match. Det betyder, at produktet skaber værdi for kunden. </p>
