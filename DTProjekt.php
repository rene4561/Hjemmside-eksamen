                        <h2>Anden halvdel</h2>
                        <p>På dag 3 i projektet startede vi på ideate. Her startede vi med endnu en standard brainstorm, hvor vi hurtigt nåede til et par konkrete løsninger. Her fandt vi det udfordrende, at blive med at have en divergent tankegang. Efter vejledning med Niels prøvede vi et par forskellige teknikker, for at vi skulle tænke i nye baner. Vi gik bl.a. en tur, mens vi snakkede om emnet. Vi prøvede også at stille nogle lidt mere ’gakkede’ spørgsmål såsom ”hvad hvis nu vores app skulle virke på månen?”. Vi stillede også nogle lidt mindre gakkede spørgsmål som ”hvad hvis nu der var 1000 brugere og ting til udlån?”. Dette hjalp os med at udvide vores idéer yderligere.</p>
                        <p>Derefter nåede vi til prototypefasen. Her skulle vi bygge et papir/papprototype med nogle interaktive elementer.  Før vi startede med produktionen af prototypen, startede vi med at lave user paths og informationsarkitektur. Dette gav os en forståelse for, hvordan alle vores idéer hang sammen. Under dette vekslede vi lidt mellem faserne prototype og ideate. </p>
                        <p>Da vi havde udarbejdet prototypen, skulle vi teste den. Testmetoden vi anvendte, var user journey – her satte vi vores testperson igennem en række scenarier, hvor de fik nogle opgaver, som de skulle løse gennem appen. Herunder observerede vi deres interaktioner med appen samt lyttede til de kommenterer, som de sagde løbene – vi bad testpersonerne at tænke højt. Efter et par tests faldt vi tilbage til prototypefasen, hvor vi rettede de problemer vores testpersonen mødte.</p>
                        <p>Som afrunding på projektet lavede vi 2 videoer – en hvor vi snakkede om de sidste 3 faser, og en hvor vi præsenterede en gennemgang af vores app.</p>
