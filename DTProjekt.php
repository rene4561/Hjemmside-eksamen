                        <h2>Første halvdel</h2>
                        <p>I den første fase, empathize, startede vi med at lave en gruppekontrakt, hvor vi fik afstemt vores forventninger til hinandens indsats. Derefter startede vi med en brainstorm, for at udforske hvilke muligheder havde. Derefter brainstormede vi adskillige gange, hvor vi gang på gang udvalgte de bedste idéer fra brainstormsessionen.</p>
                        <p>Vi endte med, at sige vi vil lave en udlånsapp til skolens udstyrsskab, med henblik på at appen skulle kunne anvendes foruden Dania Skive. Dertil var vi ude på en række institutioner, for at forhøre os i, hvordan de håndterede udlån og reservation af deres udstyr. Denne data tilegnede vi igennem en række kvalitative interviews, hvor vi snakkede med både administration, undervisere og elever/studerende. Derudover tilegnede vi os også data via ustrukturerede observationer på institutioner.  </p>
                        <p>Dernæst havde vi en opsamling på alt denne data. Den næste dag begyndte vi på define fasen. Der samlede vi hovederne, og prøvede at finde det reelle problem. Det viste sig bl.a. at én af vores første antagelser, vedr. lokaleudlejning, ikke var anset som et problem hos nogen. Dertil parkerede vi idéen på en væg med andre fravalgte idéer. </p>
