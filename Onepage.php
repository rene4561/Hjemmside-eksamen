                    <h1>One page projektet </h1>
                    <p>Med udgangspunkt i et udleveret fysisk produkt, skal der udarbejdes et one-page website, som præsenterer det udleverede produkt. Formålet kan være salg, instruktion, promotion – men ikke begrænset til disse.</p>
                    <p>Opgaven udføres i to-mandsgrupper. Deltagelse i alle faser er obligatorisk. Formålet er at lave et færdigt one-page website med relevant indhold. Opgaven dækker ikke mindst det nødvendige forarbejde i analyse og konceptdesignfaserne, inden der designes og efterfølgende produceres.</p>
                    <p>Sådan lød opgavebeskrivelsen på One page projektet. Simone, min holdkammerat, og jeg fik udleveret en engerdrik, fra Nutramino, som gik under navnet HEAT.</p>
