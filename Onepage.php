                        <h2>Forarbejde</h2>
                        <p>Design thinking tankegangen var også anvendt i dette projekt, da det både var obligatorisk samt det er et værktøj, som vi med fordel kan bruge til alle vores projekter.</p>
                        <p>Under de første faser, empathize/define, startede vi med at indsamle en masse desk reseach ved at søge på produktet. Dertil kiggede vi på de sociale medier for at undersøge de hashtags, der var printet på dåsen. Derefter udsendte vi et spørgeskema, hvor vi håbede på at blive bekræftet i nogle af de antagelser, som vi havde gjort os – dette er set i bagspejlet ikke en god metode at anvende. Denne slags kvantitative data er ikke så brugbare, som de mere kvalitative data. Efter dataindsamlingen begyndte vi at samle vores viden, dertil prøvede vi at definere vores målgruppe. Dette gav os et indblik og en visualisering af de personer, som vi tiltænkte hjemmesiden til. </p>
                        <img src="Modeller/Onepage_empazhiledefine.jpg" alt="Et billede af vores tavle under de 2 første faser">
                        <p>Under ideate begyndte vi at tænkte på, hvilken type indhold vi ville have på siden. Dertil skabte vi et moodboard, som skulle hjælpe os med at holde fokus på den målgruppe og de følelser, som vi besluttede os for at anvende. Vores målgruppe endte med at være ”power kvinder”. Dette baserede vi bl.a. på en række mega trends - primært women empowerment.</p>
                        <p>Da vi skulle lave en prototype af produktet, skitserede vi en række wireframes, som skulle fungere som skelettet til vores One page. Disse wirefames viderebyggede vi på, hvor vi gradvist forbedrede informationsarkitekturen. Dertil udarbejdede vi også et par designelementer. Idéen lød på, at hjemmesiden skulle afspejle dåsen.</p>
