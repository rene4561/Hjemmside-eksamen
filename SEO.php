                <section id="underemne1">
                    <section>
                        <p>SEO på sideniveau er kendetegnet ved, at man arbejder med sin tekstforfatning og sin HTML strukturering. Når man skriver sin webtekst, skal man helst som minimum skrive 300-400 ord – af dem burde 3-5% være søgeord. Dette har den betydning at Google ser, at siden indeholder mange af de søgeord som brugeren leder efter. Dertil kan man med fordel benytte sig af synonymer, da man aldrig kan forudse, hvad brugeren vil kalde det de leder efter. OBS – det er vigtigt at man ikke kun skriver tekst til søgemaskinen, man skal også huske brugeren.</p>
                        <p>Det er også vigtigt, og relativt nemt, at inddele sin tekst korrekt i HTML. Det der menes med dette er, at man anvender semantiske tags og nesting. Disse hjælper Google med at navigerer rundt i indholdet og udvælge hvad der er vigtigt og knapt så vigtigt, samt hvad der er relateret til hvad.</p>
                    </section>
                    <section>
                        <p>Linkbuilding dækker over, at man skaber forbindelser både internt på siden, såvel som eksternt. Disse findes i overordnede typer. </p>
                        <p>Eksterne links er dem, hvor andre hjemmesider linker til din side.</p>
                        <p>Intern linkbuilding dækker over den interne struktur, med links til andre dokumenter på samme hjemmeside.</p>
                        <p>Sidst findes der udegående links, og disse handler om at hjælpe kunden fra din hjemmeside til andre relevante hjemmesider. Alt dette vil sørge for, at Google vælger din hjemmeside. Man kan tænkte på det, som en by med god vejinfrastruktur. For god vejinfrastruktur gør at bileren kan komme, hvorhen de vil. </p>
                    </section>
                    <section>
                        <p>De tekniske barriere kan være ting som videoer, kopieret interntekst og load hastigheder. Videoer kan være problematiske af 2 årsager: for det første siger videoer ikke noget om dens indhold - for en søgemaskine som Google. Dermed får man ingen SEO fra dem. For det andet kan videofiler også fylde meget på siden. Dertil vil siden loade langsommere, og dermed vil Google nedrangere siden.</p>
