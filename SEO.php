                    <h1>SEO</h1>
                    <p>SEO, Seach Engine Optimization, kan man betragte som gratis markedsføring. Ved at optimerer sin hjemmeside efter søgemaskinerne vil man, hvis det er gjort godt, blive rangeret over sine konkurrenter, når der søges efter et produkt eller lign. Dette er netop den væsentligste forskel fra andet markedsføring. For når man laver SEO, er det kunden som henvender sig til ens hjemmeside. Der findes 3 overordnede kategorier, når man skal lave SEO – SEO på sideniveau, link building og nogle tekniske barrierer.</p>
