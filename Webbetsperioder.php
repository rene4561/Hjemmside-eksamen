                <section id="underemne1">
                    <h2>Tidlige eksperimenter (fra 1993)</h2>
                    <p>Denne periode er kendetegnet ved at alle eksperimenterede med, hvordan en hjemmeside skulle se ud og struktureres. Dette skyldes blandt andet, at det også var året hvor Mosaic blev lanceret – dette var den første browser med grafik. Der var mange begrænsninger i HTML-sproget. Derudover var det også svært at forudse hvordan browserne fortolkede siderne, da der ikke var nogen givne standarder. Dengang kunstnere fik øjnene op for den nye platform, begyndte de også at komme med bud på, hvordan man kunne bruge hjemmesider til blandt andet visuel fortælling.</p>
                    <img src="Modeller/perioder_whitehouse.png" alt="Et screenshot at det hvide huses hjemmeside under perioden">
                </section>
                <section id="underemne2">
                    <h2>Under konstruktion (fra 1996)</h2>
                    <p>Under denne periode tilsluttede mange private virksomheder sig dillen - om at få sin egen hjemmeside. Desværre var der mange, som gik i stå under udviklingen af deres hjemmeside. Dette resulterede i at mange hjemmesider fik indsat en gif, med en mand der graver, hvor teksten ”under konstruktion” stod forneden. </p>
                    <section>
                        <h2>Guruernes kamp</h2>
                        <p>Nu hvor internettet havde nået ud til pøblen, var der mange professionelle designere som også kom med deres bud på webbets muligheder. David Seiegel blev fortaler for at man skulle designe hjemmesider visuelt. Han anvendte dertil en visuel metafor for at fremme meningen med hjemmesiden. </p>
                        <img src="Modeller/perioder_guru.png" alt="Jimtown stores hjemme, under perioden, skabt af David Seiegel">
                        <p>I den anden ende af dette spektrum var danskeren Jakob Nielsen, ph.d i usabillity engeneering,  som var fortaler for funktionalitet over design. Med sin baggrund skrev han en bog, hvori han belyste vigtigheden af brugervenlighed.</p>
                        <img src="Modeller/perioder_nielsen.png" alt="Jakob Nielsens hjemmeside under perioden">
                    </section>
                </section>
                <section id="underemne3">
                    <h2>Semantisk søgning (fra 2000)</h2>
                    <p>Det var i denne periode, hvor søgemaskinen Google opstod. Det der gjorde Google til noget specielt var, at den rent faktisk kunne finde det, som brugeren søgte på. Siden anvendte en algoritme, som var i stand til at bedømme sidens indhold og betydning. Efter dette begyndte man at anvende SEO - Search Engine Optimization. </p>
                    <p>Det var også under denne periode, at man begyndte at tænke på at separere struktur og design. Deraf udviklede man CSS - Cascading Style Sheets. </p>
                </section>
                <section id="underemne4">
                    <h2>Det sociale nætværk (fra 2003)</h2>
                    <p>Under denne periode indså man at magten over internettet ikke længere lå hos de etablerede autoriteter, men hos brugerne. For at fastslå dette navngav man det nye web som web 2.0. Et nyt begreb som kom til syne, var konceptet om den lange hale.</p>
                    <img src="Modeller/perioder_denlangehale.png" alt="Modellen over den lange hale">
                    <p>Den lange hale blev først italesat af Chris Anderson. Webbet har den særlige egenskab at der, i teorien, findes uendelig lagerplads. Normale butikker har haft begrænset lagerplads, og har derfor skulle sælge noget, som de vidste at de kunne sælge. På webbet er forholdene dog ikke således. Halen er derfor en række nicheprodukter- og ydelser, som tilsammen udgør en langt større del af markedet samlet set.</p>
                    <p>Myspace kom også til under denne periode, og dette var starten på de sociale medier. Senere hen overtog Facebook markedet. Youtube, twitter m.fl. kom også frem i lyset. </p>
                </section>
                <section id="underemne5">
                    <h2>Det mobile web (fra 2010)</h2>
                    <p>Da iPhone kom til i 2007 og iPad’en i 2010, begyndte det mobile web at tage form. Alle disse nye smartphones og tablets, som begyndte at strømme ud på markedet, fik den betydning, at man nu skulle tilpasse sin hjemmeside således, at den virkede optimalt på alle skærmstørrelser.</p>
